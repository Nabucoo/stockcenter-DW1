export class Produto {
    constructor(nome, quantidade, precoCompra, precoVenda, departamento, ativo) {
        this.nome = nome;
        this.quantidade = quantidade;
        this.precoCompra = precoCompra;
        this.precoVenda = precoVenda;
        this.departamento = departamento;
        this.ativo = ativo;
    }
}